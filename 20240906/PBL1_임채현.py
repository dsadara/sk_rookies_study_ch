# 사용자 입력을 통한 합계, 최대값, 최소값 계산 프로그램
# 사용자로부터 숫자를 계속 입력받아 합계, 최대값, 최소값을 구하는 프로그램을 작성하세요.
# 사용자가 '0'을 입력하면 입력을 중단하고 최종 합계, 최대값, 최소값을 출력합니다.
# ★문제 가이드★
# 1.   숫자 입력 반복: 사용자로부터 숫자를 계속 입력받습니다.
# 2.   입력 중단 조건: 사용자가 '0'을 입력하면 입력을 중단합니다.
# 3.   최대값, 최소값 계산: 입력된 숫자들 중에서 최대값과 최소값을 계산합니다.
# 4.   결과 출력: 입력이 끝나면 최종 합계, 최대값, 최소값을 출력합니다.

number_list = []

while True:
    number = int(input("숫자를 입력하세요(0을 입력하면 종료): "))
    if number == 0:
        break
    number_list.append(number)

print(f"최종 합계: {sum(number_list)}")
print(f"최대값: {max(number_list)}")
print(f"최소값: {min(number_list)}")